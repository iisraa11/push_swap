#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*sub;
	size_t	sub_size;
	size_t	s_len;

	if (s == NULL)
		return (NULL);
	s_len = ft_strlen(s);
	if (start >= s_len)
		return (ft_strdup(""));
	if (len > s_len - start)
		len = s_len - start;
	sub_size = (len + 1);
	sub = (malloc(sub_size));
	if (sub == NULL)
		return (NULL);
	ft_strlcpy(sub, s + start, sub_size);
	return (sub);
}
