#include "libft.h"

t_list	*ft_lstnew(int content)
{
	t_list	*node;

	node = malloc(sizeof(t_list));
	if (!node)
		return (NULL);
	node->value = content;
	node->next = NULL;
	node->prev= NULL;
	return (node);
}
